    public void loaddata(){
        DefaultTableModel model = (DefaultTableModel)table_danhsach.getModel();
         try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            if (conn!=null){
                Statement dd = conn.createStatement();            
                 ResultSet  kq = dd.executeQuery("select loaivanban,mavanban,ngaybaotri,idgiaovien from xuatvanban");
                 while(kq.next()){
                    String loaivanban = kq.getString(1);
                    String mavanbang=kq.getString(2);
                    String ngaybaotri=kq.getString(3);
                    String idgiaovien=kq.getString(4);
                    String str[] = ngaybaotri.toString().split("-");
                    String ngaybaotri1=str[2]+"-"+str[1]+"-"+str[0];
                    Statement da = conn.createStatement(); 
                    ResultSet  csvc = da.executeQuery("select tengiaovien from taikhoan where idgiaovien like '"+idgiaovien+"'");
                    while(csvc.next()){
                      String tengiaovien=csvc.getString(1);
                      model.addRow(new Object[]{loaivanban, mavanbang,ngaybaotri1,tengiaovien});
                 }
                   
                }
                
            }
                      
        }catch(Exception ex){
            System.out.print(ex.toString());
        }
    }
