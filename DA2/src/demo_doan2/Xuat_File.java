    private void btn_thongtinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_thongtinActionPerformed
        // TODO add your handling code here:
        
        try{
        int Row = table_danhsach.getSelectedRow();
        String mavanban = (String) table_danhsach.getValueAt(Row,1);
        String loaivanban = (String) table_danhsach.getValueAt(Row,0);
        if(loaivanban.equals("Biên bản bảo trì")){
        Xuat_File.this.setVisible(false);
        new Bien_Ban_Bao_Tri(mavanban.trim()).setVisible(true);
        }else if(loaivanban.equals("Biên bản kiểm kê")){
             Xuat_File.this.setVisible(false);
        new Bien_Ban_KK(mavanban.trim()).setVisible(true);
        }
        }catch(Exception ex){
           JOptionPane.showMessageDialog(null,"Vui lòng chọn 1 biên bản trong danh sách!","Thông báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_thongtinActionPerformed

